public class variabel {
    
    public static void main(String[] args){
        //macam macam variabel
        String str="hai";
        int num=10;
        char character='A';
        boolean myBool=true;
        float myFloat=20.2f;
        System.out.println(str + "\n" +  num + "\n" + character + "\n" + myBool + "\n"+ myFloat);

        //multiple variabel
        int x, y, z;
        x=2;
        y=3;
        z=4;
        System.out.println(x + y + z);
    } 
}
